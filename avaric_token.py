def process_line(line):
    """
    Обрабатывает строку: 
    - заменяет каждое слово, которое является числительным, на токен 'di',
    - заменяет каждое слово, которое является местоимением, на токен 'pron'.
    """
    tokens = line.split()
    processed_tokens = []
    for token in tokens:
        if token in numerals:
            processed_tokens.append("di")
        elif token in pronouns:
            processed_tokens.append("pron")
        else:
            processed_tokens.append(token)
    return " ".join(processed_tokens)

def process_file(input_file, output_file):
    """
    Читает входной файл (одна строка = одно предложение), 
    обрабатывает каждую строку и записывает результат в выходной файл.
    """
    with open(input_file, "r", encoding="utf-8") as fin, \
         open(output_file, "w", encoding="utf-8") as fout:
        for line in fin:
            line = line.strip()  # Удаляем лишние пробельные символы
            if not line:
                continue  # Пропускаем пустые строки
            processed_line = process_line(line)
            fout.write(processed_line + "\n")

